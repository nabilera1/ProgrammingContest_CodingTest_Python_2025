import random


def main():
    # =================================================================
    # [설정] 1학년 1반 역사 인물 명단 (1101 ~ 1114)
    # =================================================================
    # 초기 데이터는 학번 순서대로 정렬되어 있습니다.
    initial_student_list = [
        "1101 세종대왕", "1102 이순신", "1103 안중근", "1104 유관순",
        "1105 김구", "1106 신사임당", "1107 장영실", "1108 정약용",
        "1109 광개토대왕", "1110 을지문덕", "1111 강감찬", "1112 단군왕검",
        "1113 김유신", "1114 계백"
    ]
    # =================================================================

    while True:
        # 1. 프로그램 시작 및 전체 명단(학번순) 출력
        print("\n\n")
        print("========================================")
        print("      역사 인물 발표 순서 추첨기      ")
        print("========================================")
        print("\n[ 전체 학생 명단 (학번순) ]")

        # 명단이 섞여있을 수 있으므로 학번순 정렬 후 출력
        initial_student_list.sort()
        for s in initial_student_list:
            print(f" - {s}")

        print("\n========================================")
        print(" 위 명단으로 발표 순서 추첨을 시작합니다.")
        print("========================================")

        input(">> 시작하려면 [Enter]를 누르세요...")

        # 2. 리스트 복사 및 셔플 (랜덤 섞기)
        # 원본 리스트를 유지하기 위해 복사본(draw_list)을 사용합니다.
        draw_list = initial_student_list[:]
        random.shuffle(draw_list)

        # 3. 한 명씩 순서 공개
        print("\n>>> 추첨을 진행합니다. (Enter 키로 진행)")

        for i, student_name in enumerate(draw_list):
            rank = i + 1
            input()  # 사용자가 엔터를 칠 때까지 대기

            print(f"[ {rank}번째 발표자 ] ----------------> {student_name}")

        print("\n")
        print("========================================")
        print("          모든 순서가 정해졌습니다.       ")
        print("========================================")
        input(">> 전체 결과를 정리해서 보려면 [Enter]...")

        # 4. 최종 결과 요약 출력
        print("\n\n")
        print("########################################")
        print("#         [ 최종 발표 순서표 ]         #")
        print("########################################")

        for i, student_name in enumerate(draw_list):
            print(f"  {i + 1:02d}번 순서 : {student_name}")

        print("########################################")

        # 5. 재시작 여부 확인
        print("\n")
        user_input = input(">> 다시 추첨하시겠습니까? (y/n): ")

        if user_input.lower() != 'y':
            print("\n프로그램을 종료합니다. 수고하셨습니다!")
            break  # while 반복문 탈출 -> 프로그램 종료


if __name__ == "__main__":
    main()


