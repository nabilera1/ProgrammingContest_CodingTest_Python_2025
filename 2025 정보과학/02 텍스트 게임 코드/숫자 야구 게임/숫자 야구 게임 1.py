import random

# 1. 게임 준비: 정답 숫자 3개 생성 (중복 없음)
# "0123456789" 문자열 중에서 3개를 무작위로 뽑습니다.
# 예: ['1', '5', '9'] 형태로 리스트가 생성됩니다.
secret_numbers = random.sample("0123456789", 3)

# (테스트용) 정답을 미리 보고 싶으면 주석을 해제하세요
# print(f"비밀 숫자: {secret_numbers}")

print("⚾ 숫자 야구 게임을 시작합니다! ⚾")
print("0부터 9까지의 중복되지 않는 숫자 3개를 맞춰보세요.")
print("-" * 40)

try_count = 0  # 시도 횟수

# 2. 게임 시작 (무한 반복)
while True:
    try_count += 1 # 시도 횟수 증가

    # 사용자 입력 받기
    user_input = input(f"[{try_count}회차] 숫자 3개를 입력하세요 > ").strip()

    # --- 입력값 검증 (잘못된 입력 방지) ---
    if len(user_input) != 3:
        print("❌ 경고: 3자리 숫자를 입력해야 합니다.")
        continue
    if not user_input.isdigit():
        print("❌ 경고: 숫자만 입력해주세요.")
        continue
    if len(set(user_input)) != 3: # set으로 중복 제거 후 길이 확인
        print("❌ 경고: 중복되는 숫자가 있습니다.")
        continue

    # --- 스트라이크 / 볼 판정 로직 ---
    strikes = 0
    balls = 0

    # enumerate를 사용하여 인덱스(i)와 숫자(num)를 동시에 가져옴
    for i, num in enumerate(user_input):
        # 1. 스트라이크 체크: 숫자와 위치가 모두 같을 때
        if num == secret_numbers[i]:
            strikes += 1
        # 2. 볼 체크: 숫자는 정답에 포함되어 있지만, 위치가 다를 때
        elif num in secret_numbers:
            balls += 1

    # --- 결과 출력 ---
    print(f"👉 결과: {strikes} 스트라이크, {balls} 볼")

    # --- 승리 조건 확인 ---
    if strikes == 3:
        print("=" * 40)
        print(f"🎉 정답입니다! {try_count}번 만에 맞췄습니다. 축하해요! 🎉")
        print(f"정답 숫자: {''.join(secret_numbers)}")
        print("=" * 40)
        break # 게임 종료 (while문 탈출)


